package com.trj.jk.web.domain.entity.personInfo;

import java.io.Serializable;

public class InfoBean implements Serializable{
	
	private String identityInfo;
	
	private String professionInfo;
	
	private String contactsInfo;
	
	private String driveLicenceInfo;
	
	private String addressInfo;
	
	private String carInfo;
	
	private String houseInfo;
	
	private String creditCardInfo;
	
	private String CreditInformation;

	public String getIdentityInfo() {
		return identityInfo;
	}

	public void setIdentityInfo(String identityInfo) {
		this.identityInfo = identityInfo;
	}

	public String getProfessionInfo() {
		return professionInfo;
	}

	public void setProfessionInfo(String professionInfo) {
		this.professionInfo = professionInfo;
	}

	public String getContactsInfo() {
		return contactsInfo;
	}

	public void setContactsInfo(String contactsInfo) {
		this.contactsInfo = contactsInfo;
	}

	public String getDriveLicenceInfo() {
		return driveLicenceInfo;
	}

	public void setDriveLicenceInfo(String driveLicenceInfo) {
		this.driveLicenceInfo = driveLicenceInfo;
	}

	public String getAddressInfo() {
		return addressInfo;
	}

	public void setAddressInfo(String addressInfo) {
		this.addressInfo = addressInfo;
	}

	public String getCarInfo() {
		return carInfo;
	}

	public void setCarInfo(String carInfo) {
		this.carInfo = carInfo;
	}

	public String getHouseInfo() {
		return houseInfo;
	}

	public void setHouseInfo(String houseInfo) {
		this.houseInfo = houseInfo;
	}

	public String getCreditCardInfo() {
		return creditCardInfo;
	}

	public void setCreditCardInfo(String creditCardInfo) {
		this.creditCardInfo = creditCardInfo;
	}

	public String getCreditInformation() {
		return CreditInformation;
	}

	public void setCreditInformation(String creditInformation) {
		CreditInformation = creditInformation;
	}

}
